$TheOldIP = "1.1.1.1"
$TheNewIP = "2.2.2.2"
$ZoneNames = Get-DnsServerZone | Select-Object -ExpandProperty ZoneName
foreach ($ZoneName in $ZoneNames){
$ZoneHostNames = Get-DnsServerResourceRecord -ZoneName $ZoneName -RRType A | Select-Object -ExpandProperty HostName
foreach ($ZoneHostName in $ZoneHostNames){
$HostnameObjectData = Get-DnsServerResourceRecord -Name $ZoneHostName -ZoneName $ZoneName -RRType "A"
$HostnameObjectDataNew = Get-DnsServerResourceRecord -Name $ZoneHostName -ZoneName $ZoneName -RRType "A"
$ObjectDataOld = [ciminstance]::new($HostnameObjectData)
$ObjectDataNew = [ciminstance]::new($HostnameObjectDataNew)
$OldIP = $ObjectDataOld.RecordData.IPv4Address.IPAddressToString
$NewIP = $ObjectDataNew.RecordData.IPv4Address=[System.Net.IPAddress]::parse($TheNewIP)
if ($OldIP -eq $TheOldIP){
Set-DnsServerResourceRecord -OldInputObject $ObjectDataOld -NewInputObject $ObjectDataNew -ZoneName $ZoneName -PassThru -Verbose
Write-Host -ForegroundColor Green "the zone $ZoneName updated with above details."}else{Write-Host -ForegroundColor Yellow "the zone $ZoneName does not need an update"}}}
